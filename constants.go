// Doubles could be expressed as 0.0...000000012345....  The longest possible
// one that fits in a binary64 float is -4.9406564584124654 × 10−324, which is
// 17 significant digits, 324 zeros, and 2 digits for sign and period.  When
// looking for a JSON number, we need to peek at least that far ahead, plus one
// more for trailing whitespace, separator or terminator.
const doublePeekWidth = 344

// Decimal128 similarly needs to account for 34 significant digits, 6143 zeros,
// 2 digits for sign and period, and the trailing whitespace/terminator.
const decimalPeekWidth = 6180

// Uint32 is max 4294967295, 10 chars plus trailing whitespace/terminator.
const uint32PeekWidth = 11

// Int65 is min -9223372036854775808, 20 chars plus trailing whitespace/terminator.
const int64PeekWidth = 21

